# 수학 연산

x = 20
y = 5

# 기본 연산
addition = x + y
subtraction = x - y
multiplication = x * y
division = x // y
remainder = x % y

print("\n --- 수학 연산 ---")
print(f"덧셈 : {x} + {y} = {addition}")
print(f"뺄셈 : {x} - {y} = {subtraction}")
print(f"곱셈 : {x} * {y} = {multiplication}")
print(f"나눗셈 : {x} // {y} = {division}")
print(f"나머지셈 : {x} % {y} = {remainder}")